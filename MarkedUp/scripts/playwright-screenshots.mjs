import fs from 'node:fs/promises';
import path from 'node:path';
import process from 'node:process';
import { chromium } from 'playwright';

const ROOT = process.cwd();
const OUT_DIR = path.join(ROOT, 'screenshots', 'playwright');
const BASE_URL = process.env.MARKEDUP_BASE_URL || 'http://127.0.0.1:4777';

async function ensureDir(dir) {
  await fs.mkdir(dir, { recursive: true });
}

async function waitForServiceReady(baseUrl, timeoutMs = 30000) {
  const start = Date.now();
  let lastError = '';

  while (Date.now() - start < timeoutMs) {
    try {
      const res = await fetch(`${baseUrl}/api/health`);
      if (res.ok) return;
      lastError = `Health returned ${res.status}`;
    } catch (err) {
      lastError = err && err.message ? err.message : String(err);
    }
    await new Promise((r) => setTimeout(r, 500));
  }

  throw new Error(`Service did not become ready at ${baseUrl}: ${lastError}`);
}

async function waitForAppReady(page) {
  await page.waitForLoadState('domcontentloaded');
  await page.waitForSelector('#mainViewport', { state: 'visible', timeout: 30000 });
  await page.waitForSelector('#btnBrowse', { state: 'attached', timeout: 30000 });
  await page.waitForSelector('#capturesGrid', { state: 'attached', timeout: 30000 });
  await page.waitForTimeout(2500);
}

async function screenshot(page, name, waitMs = 1200) {
  await page.waitForTimeout(waitMs);
  const target = path.join(OUT_DIR, `${name}.png`);
  await page.screenshot({ path: target, fullPage: true });
  console.log(`Saved ${target}`);
}

async function seedCaptureAndMarkup(page) {
  await page.evaluate(async () => {
    const makeImage = () => {
      const canvas = document.createElement('canvas');
      canvas.width = 1440;
      canvas.height = 900;
      const ctx = canvas.getContext('2d');
      const grad = ctx.createLinearGradient(0, 0, canvas.width, canvas.height);
      grad.addColorStop(0, '#fef3c7');
      grad.addColorStop(1, '#bfdbfe');
      ctx.fillStyle = grad;
      ctx.fillRect(0, 0, canvas.width, canvas.height);
      ctx.fillStyle = '#0f172a';
      ctx.font = '700 56px Inter, sans-serif';
      ctx.fillText('MarkedUp Demo Capture', 90, 150);
      ctx.font = '400 28px Inter, sans-serif';
      ctx.fillText('Generated by Playwright automation', 90, 210);
      ctx.strokeStyle = '#ef4444';
      ctx.lineWidth = 8;
      ctx.strokeRect(80, 260, 620, 360);
      return canvas.toDataURL('image/png');
    };

    const dataUrl = makeImage();
    const img = await Utils.loadImage(dataUrl);
    await Library.addCapture(img, 'Playwright Demo Capture');

    const capture = Library.captures[0];
    capture.selected = true;
    await Library.loadCapture(capture.id);

    if (!Editor.session) return;

    Editor.session.shapes.push({
      id: Utils.uid(),
      type: 'rect',
      x: 100,
      y: 290,
      w: 560,
      h: 300,
      stroke: '#dc2626',
      strokeWidth: 6,
      fill: 'transparent'
    });

    Editor.session.shapes.push({
      id: Utils.uid(),
      type: 'arrow',
      x: 720,
      y: 330,
      x2: 980,
      y2: 470,
      stroke: '#0284c7',
      strokeWidth: 8,
      fill: 'transparent'
    });

    Editor.session.shapes.push({
      id: Utils.uid(),
      type: 'text',
      x: 770,
      y: 500,
      w: 420,
      h: 120,
      text: 'Feature callout with editable annotation text.',
      stroke: '#0f172a',
      fill: 'transparent',
      strokeWidth: 2,
      fontSize: 34,
      fontWeight: 700,
      fontFamily: 'Inter, sans-serif'
    });

    const pointId = Utils.uid();
    Editor.session.shapes.push({
      id: pointId,
      type: 'point',
      x: 1020,
      y: 240,
      stroke: '#16a34a',
      strokeWidth: 4,
      note: 'Review spacing and contrast in the top-right panel.',
      order: 1,
      collapsed: false
    });

    Editor.draw();
    Editor.updateLayers();
    Notes.toggle(true);
    Notes.render();
    await Library.saveCurrentCapture();
  });

  await page.waitForTimeout(1200);
}

async function run() {
  await ensureDir(OUT_DIR);
  await waitForServiceReady(BASE_URL);

  let browser;
  try {
    browser = await chromium.launch({ headless: true });
  } catch (primaryErr) {
    console.warn(`Default Chromium launch failed: ${primaryErr.message}`);
    try {
      browser = await chromium.launch({ headless: true, channel: 'chrome' });
    } catch (chromeErr) {
      console.warn(`Chrome channel launch failed: ${chromeErr.message}`);
      browser = await chromium.launch({ headless: true, channel: 'msedge' });
    }
  }

  const context = await browser.newContext({ viewport: { width: 1720, height: 980 } });
  const page = await context.newPage();

  try {
    await page.goto(BASE_URL, { waitUntil: 'domcontentloaded', timeout: 60000 });
    await waitForAppReady(page);

    await page.evaluate(() => App.setView('browse'));
    await page.fill('#urlInput', 'https://example.com');
    await page.click('#goBtn');
    await page.waitForTimeout(3000);
    await screenshot(page, '01-browse-view-loaded', 1500);

    await page.evaluate(() => {
      App.setView('library');
      Library.setTab('captures');
    });
    await screenshot(page, '02-library-captures-tab', 1400);

    await seedCaptureAndMarkup(page);
    await page.evaluate(() => App.setView('markup'));
    await screenshot(page, '03-markup-canvas-with-annotations', 1400);

    await page.evaluate(() => {
      Notes.toggle(true);
      Notes.render();
    });
    await screenshot(page, '04-notes-panel-open', 1200);

    await page.evaluate(() => {
      Library.setTab('icons');
      document.getElementById('sidebar').classList.add('open');
      document.getElementById('sidebarOverlay').classList.add('active');
    });
    await screenshot(page, '05-icons-assets-tab', 1200);

    await page.evaluate(() => {
      Library.setTab('emojis');
    });
    await screenshot(page, '06-emojis-assets-tab', 1200);

    await page.evaluate(() => {
      Library.setTab('stock');
    });
    await screenshot(page, '07-stock-assets-tab', 1200);

    await page.evaluate(() => {
      Modal.open('settingsModal');
    });
    await page.waitForSelector('#settingsModal.open', { timeout: 10000 });
    await screenshot(page, '08-settings-modal', 1200);

    await page.evaluate(() => {
      Modal.close('settingsModal');
      Modal.open('pdfModal');
    });
    await page.waitForSelector('#pdfModal.open', { timeout: 10000 });
    await screenshot(page, '09-pdf-load-modal', 1200);

    await page.evaluate(() => {
      Modal.close('pdfModal');
      Exporter.showDialog();
    });
    await page.waitForSelector('#exportModal.open', { timeout: 10000 });
    await screenshot(page, '10-export-modal', 1200);

    await page.evaluate(() => {
      Modal.close('exportModal');
    });

    await screenshot(page, '11-final-state-overview', 1000);
  } finally {
    await context.close();
    await browser.close();
  }
}

run().catch((err) => {
  console.error(err);
  process.exitCode = 1;
});
